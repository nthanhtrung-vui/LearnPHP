<?php 

$str = "hello word";

// strtoupper($str) chuyển chữ thường thành chữ in hoa
echo strtoupper($str).'<br>';

// strtolower($str) chuyển chữ hoa thành chữ thường
echo strtolower($str).'<br>';

// ucfirst($str) chuyển kí tự đầu thành chữ hoa
echo ucfirst($str).'<br>';

// lcfirst($str) chuyển kí tự đầu thành chữ thường
echo lcfirst($str).'<br>';

// ucwords($str) chuyển đổi tất cả các kí tự đầu tiên của các từ trong một chuỗi thành chữ in hoa
echo ucwords($str);